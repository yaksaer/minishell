#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*newlst;

	newlst = (t_list *)malloc(sizeof(t_list));
	if (!newlst)
		return (NULL);
	if (newlst)
	{
		newlst->content = content;
		newlst->next = NULL;
	}
	return (newlst);
}
